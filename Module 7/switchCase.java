public class switchCase {
    public static void main(String[] args) {
        int x = 9;

        switch(x){
            case 10:
                System.out.println("X has the value of 10");
                break;
            case 11:
                System.out.println("X has the value of 11");
                break;
            case 12:
                System.out.println("X has the value of 12");
                break;
            case 13:
                System.out.println("X has the value of 13");
                break;
            default:
                System.out.println("Error");
                break;
        }
    }
}
